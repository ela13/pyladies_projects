# Homework 04, Project 19: Sum of three figures.

fig01 = float(input('Put 1st figure: '))
fig02 = float(input('Put 2nd figure: '))
fig03 = float(input('Put 3rd figure: '))

if (fig01 + fig02 + fig03) > 10:
    print('Sum of figures is greater than 10.')
else:
    print('Sum of figures is less than 10.')